function slugify(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function buildHighlights(project) {
  return [
    `${project.category} project shaped around ${project.tags[0].toLowerCase()} work and practical delivery.`,
    `Published on GitHub as ${project.repoName}, making the codebase easy to explore from the portfolio.`,
    project.liveUrl
      ? "Includes a live deployment link for recruiters and collaborators who want to inspect the running product."
      : "Presented as a portfolio case card with a dedicated detail route and direct source-code access.",
  ];
}

